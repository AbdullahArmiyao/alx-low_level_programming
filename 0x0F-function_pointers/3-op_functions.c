#include "3-calc.h"

/**
 * op_add - function to sum numbers
 * @a: num1
 * @b: num2
 * Return: int
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function to subtract numbers
 * @a: num1
 * @b: num2
 * Return: int
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - funtion to mutiply number
 * @a: num1
 * @b: num2
 * Return: int
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function to divide
 * @a: num1
 * @b: num2
 * Return: int
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function to return the remainder of the division
 * @a: num1
 * @b: num2
 * Return: int
 */

int op_mod(int a, int b)
{
	return (a % b);
}
